public class Loops {
  public static void main(String[] args) {
    for (int i = 0; i < 10; i++) {
      System.out.println("HELLO WORLD");
    }
    int j = 0;
    while (j < 10) {
      System.out.println(j);
      j++;
    }
    do {
      System.out.println("hello");
      j++;
    } while (j < 20);
  }
}
